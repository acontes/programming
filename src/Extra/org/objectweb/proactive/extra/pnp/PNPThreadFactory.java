package org.objectweb.proactive.extra.pnp;

import java.util.concurrent.ThreadFactory;
import java.util.concurrent.atomic.AtomicInteger;


/** A custom thread factory to have nice thread names */
class PNPThreadFactory implements ThreadFactory {
    private static final AtomicInteger poolNumber = new AtomicInteger(1);
    private final ThreadGroup group;
    private final AtomicInteger threadNumber = new AtomicInteger(1);
    private final String namePrefix;
    private final boolean isDaemon;
    private final int priority;

    public PNPThreadFactory(String poolName, boolean isDaemon) {
        this(poolName, isDaemon, Thread.NORM_PRIORITY);
    }

    public PNPThreadFactory(String poolName, boolean isDaemon, int priority) {
        SecurityManager s = System.getSecurityManager();
        this.group = (s != null) ? s.getThreadGroup() : Thread.currentThread().getThreadGroup();
        this.namePrefix = poolName + " #" + poolNumber.getAndIncrement() + "-thread-";
        this.isDaemon = isDaemon;
        this.priority = priority;
    }

    public Thread newThread(Runnable r) {
        Thread t = new Thread(group, r, namePrefix + threadNumber.getAndIncrement(), 0);
        t.setDaemon(isDaemon);
        t.setPriority(priority);
        return t;
    }
}