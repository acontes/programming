package org.objectweb.proactive.core.exceptions;

public class ProActiveBadConfigurationException extends Exception {

    public ProActiveBadConfigurationException() {
        super();
    }

    public ProActiveBadConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }

    public ProActiveBadConfigurationException(String message) {
        super(message);
    }

    public ProActiveBadConfigurationException(Throwable cause) {
        super(cause);
    }

}
